    private void jBLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBLoginActionPerformed
        // TODO add your handling code here
        boolean kt = false;
        int quyen =0;
        String ten = txtuser.getText();
        String mk = String.valueOf(txtpass.getPassword());
        for(NguoiDung ND:list){
            if(ND.getTen().equalsIgnoreCase(ten) && ND.getMatKhau().equals(mk)  ){
              System.out.print(ten);
              System.out.print(mk);
              kt = true;
              quyen = ND.getQuyen();
              break;
        }
        }
         
        if (kt == true && quyen == 1){
                dispose();
                TrangChuQL trangChu = new TrangChuQL();
                trangChu.setVisible(true);
            } 
        else if(kt == true && quyen == 2){
                dispose();
                TrangChuTT trangChu = new TrangChuTT();
                trangChu.setVisible(true);
            }
        else {
            JOptionPane.showMessageDialog(rootPane, "Đăng nhập thất bại. Tên đăng nhập hoặc mật khẩu không hợp lệ!");
            txtuser.setText("");
            txtpass.setText("");
        }    
        
    }//GEN-LAST:event_jBLoginActionPerformed

