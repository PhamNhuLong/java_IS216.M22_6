    public void checkLogin() {
        
        String SQL="SELECT * FROM NguoiDung";
        try {
            pst =conn.prepareStatement(SQL);
            rs = pst.executeQuery();
            while(rs.next()){
                NguoiDung ND = new NguoiDung();
                ND.setTen(rs.getString("ten"));
                ND.setMatKhau(rs.getString("matkhau"));
                ND.setQuyen(rs.getInt("quyen"));
                list.add(ND);
//                if(ND.getTen().equalsIgnoreCase(txtuser.getText().toString()) && ND.getMatKhau().equals(txtpass.getPassword().toString())){
//                    return ND;  
//                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(TestLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
       // return null;
    }
